class Employee {
  final String name;
  final String email;
  final String id;
  final String profilePicture;
  final String phone;
  final String position;
  final String status;
  final bool isAdmin;

  Employee({
    required this.name,
    required this.email,
    required this.id,
    required this.profilePicture,
    required this.phone,
    required this.position,
    required this.status,
    required this.isAdmin,
  });
} 